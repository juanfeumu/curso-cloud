-- VISTA INTERNA RESERVA_PARKING
--------------------------------------------------------------------------------
--PENDIENTE DE ELEGIR UNO U OTRO.
CREATE OR REPLACE VIEW FORMACION_TICARUM.VW_RESERVA_PARKING AS
SELECT 
    rp.ID,
    rp.TIPO_DOCUMENTO_CLIENTE,
    rp.NUMERO_DOCUMENTO_CLIENTE,
    rp.FECHA_RESERVA,
	rp.FECHA_MODIFICACION,
    mvrp.FECHA_INICIO,
    mvrp.FECHA_FIN,
    mvrp.IMPORTE,
    rp.ESTADO_RESERVA,
    rp.ERRORES
FROM FORMACION_TICARUM.RESERVA_PARKING rp, FORMACION_TICARUM.VWEXT_RESERVA_PARKING mvrp
WHERE rp.ID_RESERVA_FORMALIZADA = mvrp.ID
UNION
SELECT 
    vwrp.ID,
    vwrp.TIPO_DOCUMENTO_CLIENTE,
    vwrp.NUMERO_DOCUMENTO_CLIENTE,
    NULL AS FECHA_RESERVA,
	NULL AS FECHA_MODIFICACION,
    vwrp.FECHA_INICIO,
    vwrp.FECHA_FIN,
    vwrp.IMPORTE,
    vwrp.ESTADO_RESERVA,
    NULL AS ERRORES
FROM FORMACION_TICARUM.VWEXT_RESERVA_PARKING vwrp
where vwrp.ID NOT IN (Select r.ID_RESERVA_FORMALIZADA FROM FORMACION_TICARUM.RESERVA_PARKING r);