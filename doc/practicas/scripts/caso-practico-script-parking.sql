COMMENT ON COLUMN FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING.NUMERO_DOCUMENTO_CLIENTE IS 'Numero de documento del cliente.';
COMMENT ON COLUMN FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING.FECHA_INICIO IS 'Fecha de inicio de la reserva';
COMMENT ON COLUMN FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING.FECHA_FIN IS 'Fecha fin de la reserva.';
COMMENT ON COLUMN FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING.ESTADO_RESERVA IS 'Estado de la reserva A(ctiva), X(Cancelada).';
COMMENT ON COLUMN FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING.IMPORTE IS 'Precio total del importe de la reserva.';

