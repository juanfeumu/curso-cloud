CREATE TABLE FORMACION_TICARUM.MUCHOVUELO_RESERVA_PARKING (   
    ID VARCHAR2(36 BYTE) NOT NULL,
    TIPO_DOCUMENTO_CLIENTE VARCHAR2(2 BYTE) NOT NULL,  -- N(NIF), E(NIE), P(PASAPORTE)
    NUMERO_DOCUMENTO_CLIENTE VARCHAR2(15 BYTE) NOT NULL,
